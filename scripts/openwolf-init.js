#!/usr/bin/env node
// scripts/openwolf-init.js
// Initializes workspace .wolf/ (shared files) + project default .wolf/ (project files).
// Safe re-run: preserves customized files; always overwrites template files.

const fs = require('node:fs');
const path = require('node:path');

const REPO_ROOT = path.resolve(__dirname, '..');
const CFG_PATH = path.join(REPO_ROOT, 'dashboard', 'data', 'openwolf-config.json');
const TEMPLATES_DIR = path.join(REPO_ROOT, 'hooks', 'openwolf', 'src', 'templates');

const WORKSPACE_FILES = {
  'OPENWOLF.md':    { source: 'template', overwrite: true  },
  'config.json':    { source: 'template', overwrite: true  },
  'identity.md':    { source: 'seed',     overwrite: false, content: seedIdentity },
  'cerebrum.md':    { source: 'seed',     overwrite: false, content: seedCerebrum },
  'buglog.json':    { source: 'seed',     overwrite: false, content: () => JSON.stringify({ version: 1, bugs: [] }, null, 2) + '\n' },
};

const PROJECT_FILES = {
  'anatomy.md':        { overwrite: false, content: () => '# Anatomy\n\n<!-- auto-generated by openwolf scan -->\n' },
  'memory.md':         { overwrite: false, content: () => '# Memory\n\n' },
  'token-ledger.json': { overwrite: false, content: () => JSON.stringify({
    version: 1,
    created_at: '',
    lifetime: {
      total_tokens_estimated: 0,
      total_reads: 0,
      total_writes: 0,
      total_sessions: 0,
      anatomy_hits: 0,
      anatomy_misses: 0,
      repeated_reads_blocked: 0,
      estimated_savings_vs_bare_cli: 0,
    },
    sessions: [],
    daemon_usage: [],
    waste_flags: [],
    optimization_report: { last_generated: null, patterns: [] },
  }, null, 2) + '\n' },
};

function seedIdentity() {
  return `# Identity\n\n- **Name:** Claude\n- **Role:** workspace-scoped pair programmer\n- **Tone:** concise, direct\n`;
}
function seedCerebrum() {
  return `# Cerebrum\n\n## Key Learnings\n\n## Do-Not-Repeat\n\n## Decision Log\n`;
}

function log(msg) { process.stdout.write(`[openwolf-init] ${msg}\n`); }

function ensureDir(p) { fs.mkdirSync(p, { recursive: true }); }

function writeIfAbsentOrAllowed(destPath, producer, overwrite) {
  const existed = fs.existsSync(destPath);
  if (existed && !overwrite) {
    log(`  preserve ${destPath}`);
    return;
  }
  const content = typeof producer === 'function' ? producer() : producer;
  ensureDir(path.dirname(destPath));
  fs.writeFileSync(destPath, content, 'utf8');
  log(`  ${existed ? 'write  ' : 'create '} ${destPath}`);
}

function initWorkspace(wolfDir) {
  log(`workspace .wolf/ -> ${wolfDir}`);
  ensureDir(wolfDir);
  ensureDir(path.join(wolfDir, 'hooks'));
  for (const [name, spec] of Object.entries(WORKSPACE_FILES)) {
    const dest = path.join(wolfDir, name);
    let producer;
    if (spec.source === 'template') {
      const src = path.join(TEMPLATES_DIR, name);
      if (!fs.existsSync(src)) throw new Error(`template missing: ${src}`);
      producer = () => fs.readFileSync(src, 'utf8');
    } else {
      producer = spec.content;
    }
    writeIfAbsentOrAllowed(dest, producer, spec.overwrite);
  }
}

function initProject(wolfDir) {
  log(`project .wolf/ -> ${wolfDir}`);
  ensureDir(wolfDir);
  ensureDir(path.join(wolfDir, 'hooks'));
  for (const [name, spec] of Object.entries(PROJECT_FILES)) {
    writeIfAbsentOrAllowed(path.join(wolfDir, name), spec.content, spec.overwrite);
  }
}

function main() {
  if (!fs.existsSync(CFG_PATH)) {
    throw new Error(`${CFG_PATH} missing — run scripts/openwolf-prompt.js first`);
  }
  const cfg = JSON.parse(fs.readFileSync(CFG_PATH, 'utf8'));
  const workspaceWolf = path.join(cfg.workspace_wolf_parent, '.wolf');
  const projectWolf = path.join(cfg.project_default, '.wolf');

  initWorkspace(workspaceWolf);
  initProject(projectWolf);
  log('done');
}

try { main(); } catch (err) {
  console.error(`[openwolf-init] ERROR: ${err.message}`);
  process.exit(1);
}
