// scripts/patches/session-start.ts.patch.js
// LOCAL MODS applied in sequence:
//   (1) auto-init project .wolf/ prelude .
//   (2) route cerebrum.md + buglog.json READS to workspace (B2 + B3).

const MARKER_AUTOINIT = '// LOCAL MOD (claude-stack): auto-init project .wolf/';
const MARKER_CEREBRUM = 'LOCAL MOD (claude-stack): session-start cerebrum â†’ workspace';
const MARKER_BUGLOG   = 'LOCAL MOD (claude-stack): session-start buglog â†’ workspace';

const IMPORT_ADD_MARKER = '/* LOCAL MOD: getWorkspaceWolfDir */';

const PRELUDE = `
${MARKER_AUTOINIT}
(function autoInitProjectWolf() {
  try {
    const projectDir = process.env.CLAUDE_PROJECT_DIR || process.cwd();
    const projectWolf = path.join(projectDir, ".wolf");
    if (fs.existsSync(projectWolf)) return;

    // Only auto-init if we can resolve stack config (i.e., inside a configured workspace)
    const cfg = (function readCfg() {
      let dir = (typeof __dirname !== "undefined") ? __dirname : process.cwd();
      for (let i = 0; i < 12; i++) {
        const p = path.join(dir, "dashboard", "data", "openwolf-config.json");
        if (fs.existsSync(p)) {
          try { return JSON.parse(fs.readFileSync(p, "utf8")); } catch {}
        }
        const parent = path.dirname(dir);
        if (parent === dir) break;
        dir = parent;
      }
      return null;
    })();
    if (!cfg || !cfg.workspace_wolf_parent) return;

    // Only auto-init if project is INSIDE the configured workspace
    const wsParent = path.resolve(cfg.workspace_wolf_parent);
    const prjParent = path.resolve(projectDir);
    const rel = path.relative(wsParent, prjParent);
    if (rel.startsWith("..") || path.isAbsolute(rel)) return;

    fs.mkdirSync(path.join(projectWolf, "hooks"), { recursive: true });
    // Seed project-scoped files with minimal valid content
    const seed = {
      "anatomy.md": "# Anatomy\\n\\n<!-- auto-generated by openwolf scan -->\\n",
      "memory.md": "# Memory\\n\\n",
      "token-ledger.json": JSON.stringify({ version: 1, created_at: "", lifetime: { total_tokens_estimated: 0, total_reads: 0, total_writes: 0, total_sessions: 0, anatomy_hits: 0, anatomy_misses: 0, repeated_reads_blocked: 0, estimated_savings_vs_bare_cli: 0 }, sessions: [], daemon_usage: [], waste_flags: [], optimization_report: { last_generated: null, patterns: [] } }, null, 2),
    };
    for (const [name, content] of Object.entries(seed)) {
      const p = path.join(projectWolf, name);
      if (!fs.existsSync(p)) fs.writeFileSync(p, content, "utf8");
    }
  } catch {
    // Silent failure â€” upstream ensureWolfDir() will handle missing dir gracefully
  }
})();
`;

const READ_ANCHOR_RE = /path\.join\(\s*wolfDir\s*,\s*["'](cerebrum\.md|buglog\.json)["']\s*\)/g;

function readReplacement(fileName) {
  const marker = fileName === 'cerebrum.md' ? MARKER_CEREBRUM : MARKER_BUGLOG;
  return `path.join(getWorkspaceWolfDir(), "${fileName}") /* ${marker} */`;
}

function ensureImport(content) {
  const importMatch = content.match(/import\s+\{[^}]*\}\s+from\s+["']\.\/shared\.js["']/);
  if (importMatch && importMatch[0].includes('getWorkspaceWolfDir')) return content;
  const patched = content.replace(
    /import\s+\{\s*([^}]*)\}\s+from\s+["']\.\/shared\.js["']/,
    (m, inner) => {
      if (inner.includes('getWorkspaceWolfDir')) return m;
      const cleaned = inner.trim().replace(/,\s*$/, '');
      return `import { ${cleaned}, getWorkspaceWolfDir ${IMPORT_ADD_MARKER} } from "./shared.js"`;
    }
  );
  if (patched === content) {
    throw new Error('session-start.ts ensureImport: ./shared.js named-import anchor not found');
  }
  return patched;
}

function applyAutoInit(content) {
  if (content.includes(MARKER_AUTOINIT)) return content;
  const lines = content.split('\n');
  let lastImportIdx = -1;
  for (let i = 0; i < lines.length; i++) {
    if (/^import\s+.*\bfrom\s+["'].+["'];?\s*$/.test(lines[i])) lastImportIdx = i;
  }
  if (lastImportIdx === -1) {
    throw new Error('session-start.ts patch anchor not found â€” no import statements detected');
  }
  lines.splice(lastImportIdx + 1, 0, PRELUDE);
  return lines.join('\n');
}

function applyReadRouting(content) {
  if (content.includes(MARKER_CEREBRUM) && content.includes(MARKER_BUGLOG)) return content;
  const matches = content.match(READ_ANCHOR_RE);
  if (!matches || matches.length === 0) {
    // No read anchors present â€” not an error; upstream may have no cerebrum/buglog reads.
    return content;
  }
  let patched = content.replace(READ_ANCHOR_RE, (_m, fileName) => readReplacement(fileName));
  patched = ensureImport(patched);
  return patched;
}

function apply(content) {
  let out = applyAutoInit(content);
  out = applyReadRouting(out);
  return out;
}

module.exports = { apply, MARKER: MARKER_AUTOINIT, MARKER_CEREBRUM, MARKER_BUGLOG };
