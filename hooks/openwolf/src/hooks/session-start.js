import * as fs from "node:fs";
import * as path from "node:path";
import { getWolfDir, ensureWolfDir, writeJSON, appendMarkdown, readJSON, timestamp, timeShort, getWorkspaceWolfDir /* LOCAL MOD: getWorkspaceWolfDir */ } from "./shared.js";
// LOCAL MOD (claude-stack): auto-init project .wolf/
(function autoInitProjectWolf() {
    try {
        const projectDir = process.env.CLAUDE_PROJECT_DIR || process.cwd();
        const projectWolf = path.join(projectDir, ".wolf");
        if (fs.existsSync(projectWolf))
            return;
        // Only auto-init if we can resolve stack config (i.e., inside a configured workspace)
        const cfg = (function readCfg() {
            // walk up from THIS hook's directory (always inside
            // Combine GitRepo), not cwd. ESM runtime has no __dirname, so the
            // old `typeof __dirname` ternary fell through to process.cwd() and
            // broke auto-init for any project other than Combine GitRepo.
            let dir;
            try {
                const u = decodeURIComponent(new URL(import.meta.url).pathname);
                // Windows: "/C:/..." -> "C:/..."
                dir = path.dirname(u.replace(/^\/([A-Za-z]):/, "$1:"));
            } catch {
                dir = (typeof __dirname !== "undefined") ? __dirname : process.cwd();
            }
            for (let i = 0; i < 12; i++) {
                const p = path.join(dir, "dashboard", "data", "openwolf-config.json");
                if (fs.existsSync(p)) {
                    try {
                        return JSON.parse(fs.readFileSync(p, "utf8"));
                    }
                    catch { }
                }
                const parent = path.dirname(dir);
                if (parent === dir)
                    break;
                dir = parent;
            }
            return null;
        })();
        if (!cfg || !cfg.workspace_wolf_parent)
            return;
        // Only auto-init if project is INSIDE the configured workspace
        const wsParent = path.resolve(cfg.workspace_wolf_parent);
        const prjParent = path.resolve(projectDir);
        const rel = path.relative(wsParent, prjParent);
        if (rel.startsWith("..") || path.isAbsolute(rel))
            return;
        fs.mkdirSync(path.join(projectWolf, "hooks"), { recursive: true });
        // Seed project-scoped files with minimal valid content
        const seed = {
            "anatomy.md": "# Anatomy\n\n<!-- auto-generated by openwolf scan -->\n",
            "memory.md": "# Memory\n\n",
            "token-ledger.json": JSON.stringify({ version: 1, created_at: "", lifetime: { total_tokens_estimated: 0, total_reads: 0, total_writes: 0, total_sessions: 0, anatomy_hits: 0, anatomy_misses: 0, repeated_reads_blocked: 0, estimated_savings_vs_bare_cli: 0 }, sessions: [], daemon_usage: [], waste_flags: [], optimization_report: { last_generated: null, patterns: [] } }, null, 2),
        };
        for (const [name, content] of Object.entries(seed)) {
            const p = path.join(projectWolf, name);
            if (!fs.existsSync(p))
                fs.writeFileSync(p, content, "utf8");
        }
    }
    catch {
        // Silent failure — upstream ensureWolfDir() will handle missing dir gracefully
    }
})();
async function main() {
    ensureWolfDir();
    const wolfDir = getWolfDir();
    // Clean up stale .tmp files left from failed atomic writes
    try {
        const files = fs.readdirSync(wolfDir);
        for (const f of files) {
            if (f.endsWith(".tmp")) {
                try {
                    fs.unlinkSync(path.join(wolfDir, f));
                }
                catch { }
            }
        }
    }
    catch { }
    const hooksDir = path.join(wolfDir, "hooks");
    const sessionFile = path.join(hooksDir, "_session.json");
    const now = new Date();
    const sessionId = `session-${now.toISOString().slice(0, 10)}-${String(now.getHours()).padStart(2, "0")}${String(now.getMinutes()).padStart(2, "0")}`;
    // Create fresh session state
    writeJSON(sessionFile, {
        session_id: sessionId,
        started: timestamp(),
        files_read: {},
        files_written: [],
        edit_counts: {},
        anatomy_hits: 0,
        anatomy_misses: 0,
        repeated_reads_warned: 0,
        cerebrum_warnings: 0,
        stop_count: 0,
    });
    // Append session header to memory.md
    const memoryPath = path.join(wolfDir, "memory.md");
    const header = `\n## Session: ${now.toISOString().slice(0, 10)} ${timeShort()}\n\n| Time | Action | File(s) | Outcome | ~Tokens |\n|------|--------|---------|---------|--------|\n`;
    appendMarkdown(memoryPath, header);
    // Check cerebrum freshness — remind Claude to learn
    try {
        const cerebrumPath = path.join(getWorkspaceWolfDir(), "cerebrum.md") /* LOCAL MOD (claude-stack): session-start cerebrum → workspace */;
        const cerebrumContent = fs.readFileSync(cerebrumPath, "utf-8");
        const stat = fs.statSync(cerebrumPath);
        const daysSinceUpdate = (Date.now() - stat.mtimeMs) / (1000 * 60 * 60 * 24);
        // Count actual entries (non-comment, non-empty lines in content sections)
        const entryLines = cerebrumContent.split("\n").filter(l => {
            const t = l.trim();
            return t.startsWith("- ") || t.startsWith("* ") || (t.startsWith("[") && t.includes("]"));
        });
        if (entryLines.length < 3) {
            process.stderr.write(`💡 OpenWolf: cerebrum.md has only ${entryLines.length} entries. Learn from this session — record user preferences, project conventions, and mistakes to .wolf/cerebrum.md.\n`);
        }
        else if (daysSinceUpdate > 3) {
            process.stderr.write(`💡 OpenWolf: cerebrum.md hasn't been updated in ${Math.floor(daysSinceUpdate)} days. Look for opportunities to add learnings this session.\n`);
        }
    }
    catch { }
    // Check buglog — remind if empty
    try {
        const buglogPath = path.join(getWorkspaceWolfDir(), "buglog.json") /* LOCAL MOD (claude-stack): session-start buglog → workspace */;
        const buglog = readJSON(buglogPath, { bugs: [] });
        if (buglog.bugs.length === 0) {
            process.stderr.write(`📋 OpenWolf: buglog.json is empty. If you encounter or fix any bugs, errors, or failed tests this session, log them to .wolf/buglog.json.\n`);
        }
    }
    catch { }
    // Increment total_sessions in token-ledger
    const ledgerPath = path.join(wolfDir, "token-ledger.json");
    const ledger = readJSON(ledgerPath, { version: 1, lifetime: { total_sessions: 0 } });
    ledger.lifetime.total_sessions++;
    writeJSON(ledgerPath, ledger);
    process.exit(0);
}
main().catch(() => process.exit(0));
